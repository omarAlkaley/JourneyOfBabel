    void DisplayPlayerData()
    {
        string playerName = PlayerPrefs.GetString("PlayerName", "");
        int day = PlayerPrefs.GetInt("BirthDay", 0);
        int month = PlayerPrefs.GetInt("BirthMonth", 0);
        int year = PlayerPrefs.GetInt("BirthYear", 0);

        if (!string.IsNullOrEmpty(playerName) && day != 0 && month != 0 && year != 0)
        {
            string zodiacSign = DetermineZodiacSign(month, day);
            fixedText = "أسمك هوا  " + playerName + ". برجك هوا " + zodiacSign;
            FixTextForUI();

            optionButton1.gameObject.SetActive(true);
            optionButton2.gameObject.SetActive(true);
            optionButton3.gameObject.SetActive(true);

            optionButton1.onClick.AddListener(() => ShowAdBeforeRandomMessage(1));
            optionButton2.onClick.AddListener(() => ShowAdBeforeRandomMessage(2));
            optionButton3.onClick.AddListener(() => ShowAdBeforeRandomMessage(3));
        }
        else
        {
            fixedText = "لم يتم إدخال بيانات اللاعب بعد";
            FixTextForUI();
        }

        yesButton.gameObject.SetActive(false);
        noButton.gameObject.SetActive(false);
        warningButton.gameObject.SetActive(false); // إخفاء الزر التحذيري بعد الاستخدام
    }

